		glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);
		
		
		

		lightingShader.use();

		mat4 view;
		view = camera.GetViewMatrix();

		mat4 projection;
		projection = perspective(radians(camera.Zoom), screenWidth / screenHeight, 0.1f, 100.0f);

